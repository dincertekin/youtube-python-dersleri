# Python'da her değişkenin bir veri tipi vardır ve bu veri tipi
# değişkenin nasıl davrandığını, ne tür veriler saklayabileceğini belirler.
# Bir kutu gibi düşünebilirsiniz, ama her kutunun üzerinde malzeme resmi var! O resim dışında bir şey koyamazsınız o kutuya.
# İşte bazı yaygın veri tipleri:

# String (Metinsel veri): Tırnak içinde yazılan karakterlerdir.
stringOrnegi = "Ali okula gitti."

# Integer (Tam sayı): Ondalıklı olmayan sayılardır.
integerOrnegi = 5

# Float (Ondalıklı sayı): Noktalı sayılar için kullanılır.
floatOrnegi = 10.45

# Complex (Karmaşık sayı): Matematiksel karmaşık sayıları ifade eder. 'j' sanal birimi temsil eder.
complexOrnegi = 12j

# List (Liste): Sıralı ve değiştirilebilir (mutable dediğimiz) veri tipidir. Köşeli parantez [] ile kullanılır.
listOrnegi = ["domates", "biber", "salatalik", "elma"]

# Tuple (Demet): Sıralı ama değiştirilemez (immutable dediğimiz) veri tipidir. Normal parantez () ile kullanılır.
tupleOrnegi = ("armut", "muz", "cilek")

# Dictionary (Sözlük): Anahtar-değer (key-value) mantığıyla veri tutar. Süslü parantez {} ile kullanılır.
dictOrnegi = {"isim": "Dincer", "soyisim": "Tekin", "yas": 21, "yer": "Ankara"}

# Set (Küme): Sırasız ve benzersiz elemanlardan oluşan veri yapısıdır. Süslü parantez {} ile kullanılır.
setOrnegi = {"eleman1", "eleman2", "eleman3"}

# Boolean (Mantıksal): Sadece True veya False değerlerini alabilir.
boolOrnegi = False

# print fonksiyonu ile üstteki gibi değişkenleri ekrana yazdırabiliriz.
print("Veri tiplerini yazdirmak icin degiskenin adini print fonksiyonu icerisine yazmamiz yeterlidir.")

